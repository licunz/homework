
#include <iostream>
using namespace std; 

int main()
{
   double r; 
   double pi = 3.14159; 

  
   cout << "Enter the radius: ";
   cin >> r;

  
   cout << "The diameter is " << r * 2;

   
   cout << "\nThe circumference is " << 2.0 * pi * r;

   
   cout << "\nThe area is " << pi * r * r << endl;
} 

